        Nom = jTextField1.getText();
        Prenom = jTextField2.getText();
        Nom_rue = jTextField3.getText();
        Num_rue = jTextField8.getText();
        Ville = jTextField4.getText();
        Code_Postal = jTextField5.getText();
        mail = jTextField6.getText();
        mot_de_passe = jTextField7.getText();
        Numero_tel = jTextField9.getText();
        String requete2 = "SELECT * FROM Utilisateurs";
        String requeteadr = "SELECT * FROM adresse";
        int id_adresse = 1;
        try {
            statement = maConnexion.ObtenirConnexion().createStatement();
            resultat = statement.executeQuery(requete2);
            while (resultat.next()) {
                id_cli = resultat.getInt("ID_Utilisateur") + 1;
            }
        } catch (SQLException ex) {
            System.out.println("Erreur : " + ex);
        }
        try {
            statement = maConnexion.ObtenirConnexion().createStatement();
            resultat = statement.executeQuery(requeteadr);
            while (resultat.next()) {
                id_adresse = resultat.getInt("ID_adresse") + 1;
            }
        } catch (SQLException ex) {
            System.out.println("Erreur : " + ex);
        }
        String requete = "INSERT INTO Utilisateurs VALUES ('" + id_cli + "','"
                + Nom + "','"
                + Prenom + "','"
                + Numero_tel + "','"
                + mail + "','"
                + mot_de_passe + "')";
        String reqadresse = "INSERT INTO adresse VALUES ('" + id_adresse + "','"
                + Num_rue + "','"
                + Nom_rue + "','"
                + Code_Postal + "','"
                + Ville + "','"
                + id_cli + "')";
        try {
            statement = maConnexion.ObtenirConnexion().createStatement();
            statement.executeUpdate(requete);
            statement.executeUpdate(reqadresse);
            JOptionPane.showMessageDialog(null, "inscription réalisée avec succès !");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erreur lors de l'inscription verifiez bien les données rentrée  : \n " + ex);
        }

