    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            statement = maConnexion.ObtenirConnexion().createStatement();
            stmnt = maConnexion.ObtenirConnexion().createStatement();
            String Trouvemailuti = "SELECT * FROM Utilisateurs";
            String Trouvemailadre = "SELECT * FROM adresse";
            resultat = statement.executeQuery(Trouvemailuti);
            resultatadre = stmnt.executeQuery(Trouvemailadre);
            String mailrentrer = jTextField10.getText();
            String mdprentrer = jPasswordField1.getText();
            while (resultat.next()) {
                String mailacomparer = resultat.getString("mail");
                String mdpacomparer = resultat.getString("mot_de_passe");
                resultatadre.next();
                if (mailacomparer.equalsIgnoreCase(mailrentrer) && mdpacomparer.equals(mdprentrer)) {
                    id_cli = resultat.getInt("ID_Utilisateur");
                    this.setVisible(false);
                    new Annonces(id_cli).setVisible(true);
                    
                }
            }
        } catch (SQLException ex) {
            System.out.println("Erreur : " + ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

